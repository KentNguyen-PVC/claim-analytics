-- ============================================
-- CLAIM TYPE
-- ============================================
CREATE TABLE CLAIM_TYPE (
    CODE            VARCHAR2(50) PRIMARY KEY,
    DESCRIPTION     VARCHAR2(255) NOT NULL
);

-- ============================================
-- CALENDAR DIMENSION (CUMULATIVE WORKING MIN)
-- IS_WORKING_DAY 1 = working day, 0 = weekend hoặc holiday
-- ============================================
CREATE TABLE CALENDAR_DIM (
    COUNTRY_CODE        VARCHAR2(10) NOT NULL,
    CAL_DATE            DATE NOT NULL,
    IS_WORKING_DAY      NUMBER(1) NOT NULL,
    WORKING_DAY_SEQ     NUMBER NOT NULL,
    WORKING_MIN_SEQ     NUMBER NOT NULL,
    HOLIDAY_NAME        VARCHAR2(255),
    VERSION_NO          NUMBER DEFAULT 1 NOT NULL,

    CONSTRAINT PK_CALENDAR_DIM
        PRIMARY KEY (COUNTRY_CODE, CAL_DATE)
)
PARTITION BY LIST (COUNTRY_CODE)
(
    PARTITION P_SG VALUES ('SG'),
    PARTITION P_MY VALUES ('MY'),
    PARTITION P_VN VALUES ('VN')
);

-- ============================================
-- CLAIM TABLE (PARTITIONED – CRITICAL)
-- ============================================
CREATE TABLE CLAIM (
    ID                      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CLAIM_NO                VARCHAR2(100) NOT NULL,
    CLAIM_TYPE              VARCHAR2(50) NOT NULL,
    COUNTRY_CODE            VARCHAR2(10) NOT NULL,
    POLICY_NO               VARCHAR2(100) NOT NULL,

    SUBMITTED_AT            TIMESTAMP,
    FINAL_DECISION_AT       TIMESTAMP,
	FINAL_STATUS            VARCHAR2(50),
    TAT_WORKING_MINUTES     NUMBER,
    TAT_CALENDAR_MINUTES    NUMBER,
    TAT_VERSION             NUMBER DEFAULT 1,

    CREATED_AT              TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT PK_CLAIM PRIMARY KEY (ID),
    CONSTRAINT FK_CLAIM_TYPE FOREIGN KEY (CLAIM_TYPE)
        REFERENCES CLAIM_TYPE(CODE)
)
PARTITION BY RANGE (FINAL_DECISION_AT)
INTERVAL (NUMTOYMINTERVAL(1,'MONTH'))
SUBPARTITION BY LIST (COUNTRY_CODE)
SUBPARTITION TEMPLATE (
    SUBPARTITION SP_SG VALUES ('SG'),
    SUBPARTITION SP_MY VALUES ('MY'),
    SUBPARTITION SP_VN VALUES ('VN'),
    SUBPARTITION SP_DEFAULT VALUES (DEFAULT)
)
(
    PARTITION P0 VALUES LESS THAN (DATE '2025-01-01')
);

ALTER TABLE CLAIM ENABLE ROW MOVEMENT;

-- ============================================
-- CLAIM STATUS HISTORY (AUDIT PURPOSE)
-- ============================================
CREATE TABLE CLAIM_STATUS_HISTORY (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CLAIM_ID        NUMBER NOT NULL,
    STATUS          VARCHAR2(50) NOT NULL,
    STATUS_AT       TIMESTAMP NOT NULL,
    IS_FINAL        NUMBER(1) DEFAULT 0 NOT NULL,

    CONSTRAINT PK_CLAIM_STATUS PRIMARY KEY (ID),
    CONSTRAINT FK_STATUS_CLAIM FOREIGN KEY (CLAIM_ID)
        REFERENCES CLAIM(ID)
);

-- ============================================
-- HOLIDAY
-- ============================================
CREATE TABLE HOLIDAY (
    COUNTRY_CODE VARCHAR2(10),
    HOLIDAY_DATE DATE,
    NAME VARCHAR2(255),
    SOURCE VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (COUNTRY_CODE, HOLIDAY_DATE)
);