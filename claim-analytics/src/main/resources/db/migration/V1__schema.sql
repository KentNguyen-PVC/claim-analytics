-- ============================================
-- CALENDAR DIMENSION
-- IS_WORKING_DAY 1 = working day, 0 = weekend hoặc holiday
-- ============================================
CREATE TABLE CALENDAR_DIM (
    COUNTRY_CODE        VARCHAR2(10) NOT NULL,
    CAL_DATE            DATE NOT NULL,
    IS_WORKING_DAY      NUMBER(1) NOT NULL,
    WORKING_DAY_SEQ     NUMBER NOT NULL,
    WORKING_MIN_SEQ     NUMBER NOT NULL,
    HOLIDAY_NAME        VARCHAR2(255),
    VERSION_NO          NUMBER DEFAULT 1 NOT NULL,

    CONSTRAINT PK_CALENDAR_DIM
        PRIMARY KEY (COUNTRY_CODE, CAL_DATE)
)
PARTITION BY LIST (COUNTRY_CODE)
(
    PARTITION P_SG VALUES ('SG'),
    PARTITION P_MY VALUES ('MY'),
    PARTITION P_VN VALUES ('VN')
);

-- ============================================
-- POLICY TABLE
-- ============================================
CREATE TABLE POLICY (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POLICY_NUMBER VARCHAR2(50) NOT NULL,
    STATUS VARCHAR2(20) NOT NULL,
    COUNTRY_CODE VARCHAR2(10) NOT NULL,
    EFFECTIVE_DATE TIMESTAMP WITH TIME ZONE NOT NULL,
    EXPIRY_DATE TIMESTAMP WITH TIME ZONE NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE NOT NULL
);

-- Unique constraint for policy number
ALTER TABLE POLICY ADD CONSTRAINT UK_POLICY_NUMBER UNIQUE (POLICY_NUMBER);

-- ============================================
-- CLAIM TABLE
-- ============================================
CREATE TABLE CLAIM (
    CLAIM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POLICY_ID NUMBER NOT NULL,
    CLAIM_NUMBER VARCHAR2(100) NOT NULL,
    CLAIM_DATE DATE NOT NULL,
    CLAIM_AMOUNT NUMBER(15,2) NOT NULL,
    APPROVED_AMOUNT NUMBER(15,2),
    CLAIM_STATUS VARCHAR2(20) NOT NULL,
    CLAIM_TYPE VARCHAR2(30) NOT NULL,
    DESCRIPTION VARCHAR2(500),
    CREATED_AT TIMESTAMP WITH TIME ZONE NOT NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE NOT NULL,

    FINAL_DECISION_AT TIMESTAMP,
    TAT_WORKING_MINUTES NUMBER,

    CONSTRAINT FK_CLAIM_POLICY FOREIGN KEY (POLICY_ID) REFERENCES POLICY(ID)
)
PARTITION BY RANGE (FINAL_DECISION_AT)
INTERVAL (NUMTOYMINTERVAL(1,'MONTH'))
(
    PARTITION P0 VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00')
);
ALTER TABLE CLAIM ENABLE ROW MOVEMENT;
-- check constraint for status
ALTER TABLE CLAIM ADD CONSTRAINT CHK_CLAIM_STATUS CHECK (CLAIM_STATUS IN ('SUBMITTED', 'APPROVED', 'REJECTED'));
ALTER TABLE CLAIM ADD CONSTRAINT UK_CLAIM_NUMBER UNIQUE (CLAIM_NUMBER);

-- ============================================
-- CLAIM STATUS HISTORY
-- ============================================
CREATE TABLE CLAIM_STATUS_HISTORY (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLAIM_ID NUMBER NOT NULL,
    FROM_STATUS VARCHAR2(20),
    TO_STATUS VARCHAR2(20) NOT NULL,
    CHANGED_AT TIMESTAMP WITH TIME ZONE NOT NULL,
    IS_FINAL NUMBER(1),
    NOTE VARCHAR2(1000)
);

-- Foreign key to CLAIM table
ALTER TABLE CLAIM_STATUS_HISTORY ADD CONSTRAINT FK_HISTORY_CLAIM FOREIGN KEY (CLAIM_ID) REFERENCES CLAIM(CLAIM_ID);

-- ============================================
-- HOLIDAY
-- ============================================
CREATE TABLE HOLIDAY (
    COUNTRY_CODE VARCHAR2(10),
    HOLIDAY_DATE DATE,
    NAME VARCHAR2(255),
    SOURCE VARCHAR2(100),
    CREATED_AT TIMESTAMP WITH TIME ZONE NOT NULL,
    PRIMARY KEY (COUNTRY_CODE, HOLIDAY_DATE)
);